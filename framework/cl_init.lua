local localstore = zm or {};
zm = localstore or {};
zm.uniqueID = zm.uniqueID or 0;

--[[-------------------------------------------------------------------------
Logs functions
---------------------------------------------------------------------------]]
function zm:ConsoleLog(text)
	MsgC(Color(255,0,0), "[Zombie Plague] ", Color(255,255,255), text, Color(255,0,0), ";", "\n");
end;

--[[-------------------------------------------------------------------------
Local data
---------------------------------------------------------------------------]]
function zm:SendLocalData()
	local defZombieClass = cookie.GetString(self.uniqueID.."_zombieClass", self.cfg.DefaultZMClass);

	netstream.Start("zm:localInfo", {
		["defZombieClass"] = defZombieClass
	});
end;

